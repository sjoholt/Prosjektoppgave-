           ,'<p>Det er vel å banne i kjerka. Men AC/DC på Valle Hovin i 2015 var faktisk et par hakk bedre enn AC/DC på Valle Hovin i 2010.
Dette til tross for at de nå reiser rundt uten den suverent hardtslående, men bedagelige trommeslageren Phil Rudd og – aller verst – gode broder Malcolm: Sologitaristen Angus Youngs stødige, stoiske bror, og mange menneskers absolutte favorittmedlem i AC/DC.
Han var sårt savnet, trass i at familiebedriftens nevø, Stevie Young, så absolutt ikke gjorde skam på seg (eller dem).</p>

<p>På den annen side: Et utpreget veldrevet og usentimentalt band som AC/DC, lar seg ikke lett vippe av pinnen.
De overlevde den uerstattelige vokalisten Bon Scotts død i 1980. Og de har overlevd det siste, elendige året i sin over 40 år lange historie.
Sist de var her, var flyet deres sterkt forsinket.
Det bar konserten preg av. Showet ble vingeklippet; vi ble snytt for den tradisjonelle avslutningen (ritualer er hardrockens livsblod, og en AC/DC-konsert skal avsluttes med «For Those About To Rock (We Salute You»)».
Bandet syntes rent generelt å lide under hastverk.</p>

<p>Ikke så i kveld, da bandet hadde all verdens tid, og fikk gjort alt de skulle og vel så det (Angus har droppet det gamle «strippenummeret», men det er greit. Det var ikke så gøy).
Brian Johnson var til tider ustabil. Rotet med tekstene, fikk ikke finjustert den umenneskelige tåkeluren av en røst han har.
Men Angus Young var i sitt seksti år gamle livs form, og det er sannelig det viktigste i denne sammenhengen.
Han er et absurd syn: En knøttliten kar i skoleuniform, med en SG over kroppen som er nesten like stor som ham.
Men han personifiserer en viss type gitarhelt: Han som er opptatt av fart og intensitet og fjellstøtt «groove».
I 2015, mer enn noen gang før, er AC/DC hans band, og hans band alene. I kveld forvaltet han familiearven på beste vis. </p>

<p><strong>Konserten låt for låt:</strong></p>

<P><strong>«Rock Or Bust»</strong><br>
Artig «forfilm», hvori menn går på månen (jubileum nå førstkommende mandag, folkens) og finner … et brennende krater med AC/DC-logo. Så fyrverkeri, og Angus trippende ut på scenen. Skoleuniformen er sort i aften, lua og slipset irrgrønt. Det låter som AC/DC, kan du si. </p>

<p><strong>«Shoot To Thrill»</strong><br>
Brian Johnson ser ut som en bryggesjauer, og låter – ikke helt upassende – som en tåkelur som er gått tom for Cosylan. Chris Slade er en mer «proaktiv» trommeslager enn den guddommelig «laidback» Phil Rudd (som jo dog har mange andre, dårligere sider). Angus gjør den frenetiske «duckwalken» sin med uforminsket energi, og spiller sin første skikkelige solo. Kveldens første koordinerte klappsalver er også et faktum.</p>

<p><strong>«Hell Ain’t A Bad Place To Be»</strong><br>
Angus er i gang med den speedfrikete munntyggingen sin, lua er falt av og et høyt hårfeste kommet til syne. Man har fått summet seg, og registrert at det jo dessverre ikke er Malcolm Young som står der til venstre, foran den massive veggen av falske Marshall-forsterkere. Men snarere nevøen Steve «Stevie» Young». Han gjør jobben sin godt, det er ikke det.</p>

<p><strong>«Back In Black»</strong><br>
Litt av en luksus å kunne brenne av en slik vinner som låt nummer fire. AC/DCs mest funky låt (hvem har ikke glemt de norske brødrene, eh, Brothers funktolkning fra det tidlige nittitallet?) besitter ikke bare ett, men tre ur-gitarriff. Og Slade slår den som Rudd ville ha gjort – tungt, enkelt, svingende.</p>

<p><strong>«Play Ball»</strong><br>
Den beste fra bandets siste, «Rock Or Bust». En ublu søknad om å overta The White Stripes’ «Seven Nation Army»s posisjon som alle amerikanske sportsevenementers favorittsang. Lyden er høy, men ikke vondt høy, og Angus spiller den lille gitarfiguren etter refrenget nesten grasiøst. Han spiller aldri surt, eller «feil». Det gjorde ikke broren hans heller.</p>

<p><strong>«Dirty Deeds Done Dirt Cheap»</strong><br>
Fra den gangen da AC/DC nesten låt som et punkband: Skitten, hissig, full av overstyrt energi. Johnson danser lidderlig rundt Young (de to er visst ikke verdens beste venner); Young stikker av og ut på venstreflanken, giktisk labbende som en zombie med en Gibson SG. Han tar en solo helt alene på tampen, og demonstrerer sin absolutte kontroll over instrumentet.</p>

<p><strong>«Thunderstruck»</strong><br>
Den mest harry av alle AC/DCs utallige harry låter (det er ment som en kompliment). Det er som om en professor er gått inn i et laboratorium med kun én ting for øyet: Å fremstille tidenes mest idiotsikre stadionrockknuser, med The Who-parafraseringer og det hele. Resultatet knuser denne stadion, for å si det pent. Youngs «duckwalk» filmes fra undersiden. Det er liv ved de 60.</p>

<p><strong>«High Voltage»</strong><br>
Det finnes mennesker i verden som foretrekker Johnson fremfor bandets gamle vokalist Bon Scott (jeg kjenner selv to). I denne synger han småfalskt til tider, når han ikke prøver å få publikum til å messe «Malcolm Young» i den pliktskyldige allsangavdeling. Litt utspilt låt også, må det være lov å mene. Men. «It’s rock’n’roll», mener Johnson. Og det er det jo.</p>

<p><strong>«Rock’n’Roll Train»</strong><br>
Den eneste fra bandets forrige album, «Black Ice», som har fått lov å være med videre. Så er den da også dét overlessede albumets suverent beste låt. Koringen, ved Stevie og bassist Cliff Williams, høres imidlertid merkelig hyeneaktig ut. Og selv om den var best på «Black Ice», betyr ikke det at den er sånn best best.</p>

<p><strong>«Hells Bells»</strong><br>
Denne, derimot, er best best. Bandets «svarteste», seigeste låt. Den svære kirkeklokken svinger faretruende over scenen. Ingen ulykker nå, vær så snill: Man husker den fatale U2-konserten her opp på slutten av nittitallet, da bandet gikk inn i en stor plastikksitron, og ikke kom seg ut igjen. I gamle dager ville Johnson ha besteget klokken, og svingt med den. Det gjør han ikke lenger. Men ellers er dette sånn rock som stadioner ble bygd for.</p>

<p><strong>«Baptism By Fire»</strong><br>
Dusinvare fra «Rock Or Bust». Det er blitt mørkt nok til at man kan begynne å nyte de mange røde, lysende djevelhornene i plast ute i publikumsmassen – bandets beste «merch»-produkt. AC/DC er et typisk «T-skjorteband». Et slikt orkester folk går og ser iført turneskjorten fra den forrige turneen (eller en tidligere). Men det er forbløffende få slike å se her i kveld. Foruroligende!</p>

<p><strong>«You Shook Me All Night Long»</strong><br>
Johnson klarer å bomme i det første verset her, i en låt han har sunget i 35 år. Folk kunne ikke bry seg mindre, og danser – vel, hopper opp og ned ­– som besatte, med eller uten innsmuglede selfiestenger. Kan ikke fri meg for tanken på at Rudd ville ha fått ytterligere schwung over denne.</p>

<p><strong>«Sin City»</strong><br>
Johnson synger i perioder «normalt», altså uten den verste forkjølelsen, i denne. Han går bort på venstre side og klager på noe, sikkert at han ikke kan høre sin egen heshet i monitor eller noe. Akk! Dømt til å synge som et normalt menneske. Det bedrer seg. Men noe rusk i maskineriet her. Det kan skje med selv de mest rutinerte stadionbikkjene.</p>

<p><strong>«Shot Down In Flames»</strong><br>
Bandets tandre ballade om å gå ut på byen og jakte «good love», bare for å bli avvist på det mest brutale vis – i flammer! Rutinen er tilbake. Men låten blir ikke så mye mer enn nettopp rutine.</p>

<p><strong>«Have A Drink On Me»</strong><br>
Stevie Young beveger seg noe mer enn salige Malcolm. Det innebærer at han beveger seg et par meter til venstre eller høyre eller frem eller tilbake. Malcolm nøyde seg stort sett med å stikke hodet frem mot mikrofonen når han skulle kore, samt trampe frenetisk med føttene. «Have A Drink On Me» er ikke det sterkeste kortet fra «Back In Black» (1980). På den annen side begynner scenen, som så ganske naken ut innledningsvis, å nyte godt av lysproduksjonen nå. Og snart kommer kanonene!</p>

<p><strong>«T.N.T.»</strong><br>
En ultimat neandertalerrocker, og en destillasjon av bandets basale appell. Johnson virker ikke hundre prosent tilstedeværende, liksom. Men Young er svært løs i snippen, med skjorta for lengst utenpå kortbuksene, spesielt i avslutningen, da tempoet dobles og gitarspillet blir kakofonisk.</p>

<p><strong>«Whole Lotta Rosie»</strong><br>
Et gigantisk oppblåsbart kvinnemenneske i flosshatt (hvorfor floss?) og det man i gamle dager kalte et «raffsett» dukker opp der bak. Hun har dollarsedler stukket ned i nettingstrømpene, og stryker seg «forførerisk» over låret. Kvinnesynet er kan hende semmert her i gården, men rocken er upåklagelig. Youngs iltre solo føles som ildkuler i ørene – lyden er blitt gradvis høyere – og avslutningen er kontrollert kaos. Stas.</p>

<p><strong>«Let There Be Rock»</strong><br>
Skapelsesberetningen, boogie-style. En fyr til høyre her blir så forfjamset av begeistring (og kanskje litt øl) at han faller bakover på rumpa på tribunen. Hvite lys blender publikum, strategisk nok idet Johnson befaler «let there be light!». Young innleder en lang, lang solo. Den er innledningsvis en kavalkade over boogierockens alle triks, fremført med et overskudd som må sies å være fantastisk etter så mange år i faget. Svetten renner, og jeg undrer meg over at blodet i fingrene hans ikke gjør det samme. Han spiller med kun venstrehånden (bedre enn de fleste spiller med to); faller til gulvet, bukter seg rundt på gulvet som et insekt i rigor mortis, spretter opp igjen og løper opp over trommene for å avslutte helt alene, hele tiden trippende på de korte beina sine. For en oppvisning. Bare å ta av seg djevelhornene i respekt.</p>

<p><strong>«Highway To Hell»</strong><br>
Tanken på at det kanskje er den siste gang vi ser dette i Norge – flammene på scenen, publikum badet i rødt, de blinkende djevelhornene, åtti tusen armer i været som feirer det faktum at vi er på vei lukt til helvete – gjør denne ekstra, eh, søt. Prikkfritt spilt.</p>

<p><strong>«For Those About To Rock (We Salute You)»</strong><br>
 Kanoner! (Tolv i tallet; seks bakpå scenen og seks ute på sidene). Johnsons aller mest blodskutte, ballesprengte, pissetrengte «yeah, yeah, yeeeeeah». Kanonene låter relativt spinkle, det er så. Men det gjør ikke Angus, som står for nok en oppvisning på SGen. Godt å få sett dette på norsk jord igjen. Forrige gang de var her oppe, var de sterkt forsinket på grunn av sen landing på Gardemoen – og vi ble snytt for avslutningen (og kanonene!).</p>
